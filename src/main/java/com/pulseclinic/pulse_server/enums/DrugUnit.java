package com.pulseclinic.pulse_server.enums;

public enum DrugUnit {
    TABLET,        // per tablet
    CAPSULE,       // per capsule
    ML,            // per milliliter (liquid)
    G,             // per gram (cream, powder)
    AMP,           // per ampoule (injection)
    VIAL,          // per vial
    BOTTLE,        // per bottle (liquid)
    TUBE,          // per tube (cream/gel)
    BOX,           // per box (multiple items)
    PACK,          // generic packaging
    STRIP          // 10 tablets per strip, etc.
}
